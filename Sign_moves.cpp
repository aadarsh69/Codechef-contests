#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int nn;
        cin >> nn;
        int p = 0;
        if (nn >= 0)
        {
            p--;
        }
        else
        {
            p++;
        }
    }
    return 0;
}